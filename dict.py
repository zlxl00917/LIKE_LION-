# 딕셔너리 -> 딕셔너리는 key : value 의 페어다!

user_dictionary = {
    'name': 'juwon',
    'age': '24',
    'school': 'korea university'
}
print('\n')
print(user_dictionary, '\n')

user_name = user_dictionary['name']
user_age = user_dictionary['age']
user_school = user_dictionary['school']

print('이름은 ? ', user_name, '\n')
print('나이는 ? ', user_age, '\n')
print('학교는 ? ', user_school, '\n')
